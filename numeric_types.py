# Belajar Numeric Types

# 1. Integer
# 2. Float
# 3. Complex

# Contoh penggunaan tipe data numerik
angka1 = 1       # Integer
angka2 = 1.5     # Float
angka3 = 2 + 3j  # Complex

# Operasi numerik
total = angka1 + angka2  # Operasi penjumlahan antara integer dan float
hasil_bagi = 10 / 3      # Operasi pembagian
hasil_complex = angka3 + (1 + 2j)  # Operasi dengan complex number

# Menampilkan hasil
print(f"Total: {total}")  
print(f"Hasil pembagian 10/3: {hasil_bagi}")  
print(f"Hasil operasi kompleks: {hasil_complex}")  
