# Belajar String Methods
# Referensi: https://docs.python.org/3.7/library/stdtypes.html#string-methods

# Deklarasi string
nama = "Alief Chandra Darmawan"

# Menampilkan string asli
print(f"Original: {nama}")

# Menggunakan berbagai metode string
print(f"Uppercase: {nama.upper()}")       # Mengubah semua huruf menjadi kapital
print(f"Capitalize: {nama.capitalize()}") # Huruf pertama menjadi kapital
print(f"Title: {nama.title()}")           # Setiap kata diawali huruf kapital
print(f"Split: {nama.split(' ')}")        # Memisahkan string berdasarkan spasi
