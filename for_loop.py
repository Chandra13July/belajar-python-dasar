# Belajar For Loop

# Daftar pelanggan
pelanggan = ["Alief", "Chandra", "Darmawan", "Mary"]

# Menambahkan pelanggan baru
pelanggan.extend(["Alex", "Raffi"])

# Menampilkan semua nama pelanggan
print("Daftar Pelanggan:")
print("========================")
for nama in pelanggan:
    print(f"Nama Pelanggan : {nama}")
    print("========================")
