# Operator Perbandingan

# Simbol perbandingan:
# >  : lebih dari
# <  : kurang dari
# >= : lebih dari sama dengan
# <= : kurang dari sama dengan
# == : sama dengan
# != : tidak sama dengan

# Contoh perbandingan angka
print("Perbandingan Angka:")
print(1 > 4)   # False
print(1 < 4)   # True
print(1 >= 1)  # True
print(1 <= 1)  # True
print(10 == 10)  # True
print(10 != 10)  # False

# Contoh perbandingan string
print("\nPerbandingan String:")
print("alief" == "alief")      # True
print("chandra" != "chandra")  # False
